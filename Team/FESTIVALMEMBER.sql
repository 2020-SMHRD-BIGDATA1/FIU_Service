CREATE TABLE FESTIVALMEMBER (
	FEST_ID varchar2(20) NOT NULL PRIMARY KEY,
	FEST_PW varchar2(20) NOT NULL,
	MEMBER_NAME varchar2(20) NOT NULL,
	phone varchar2(20) NOT NULL,
	location varchar2(100) NOT NULL,
	sex varchar2(60) NOT NULL,
	age varchar2(10) NOT NULL
)

CREATE TABLE FESTIVAL_INFORMATION(
	FEST_NO NUMBER(7) NOT NULL PRIMARY KEY,
	FEST_NAME VARCHAR2(70) NOT NULL,
	CITY VARCHAR2(10) NOT NULL,
	FEST_TEL VARCHAR2(20) NOT NULL,
	FEST_DATE1 DATE NOT NULL,
	FEST_DATE2 DATE NOT NULL,
	FEST_CONTENTS VARCHAR2(1000),
	FEST_CONTENTS2 VARCHAR2(2100)
)

CREATE TABLE FESTIVAL_REVIEW(
	REV_NO NUMBER(11) NOT NULL PRIMARY KEY,
	CITY VARCHAR2(10) NOT NULL ,
	REV_CON VARCHAR2(10) NOT NULL,
	AGE VARCHAR2(10) NOT NULL,
	REV_SCORE NUMBER(2,1) NOT NULL,
	REV_DATE DATE NOT NULL
)

